<template>
    <div style=" text-align :center;">
        <h1>学生成绩列表</h1>
        <el-button type="danger" @click="getStudentList">获取学生成绩</el-button>
        <!--p>{{students}}</!--p-->
        <table class="table">
            <thead>
                <tr>
                    <th>学号 </th>
                    <th>姓名 </th>
                    <th>性别 </th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="stu in students" :key="stu.id">
                    <th>{{stu.id}}</th>
                    <th>{{stu.name}}</th>
                    <th>{{stu.gender}}</th>
                </tr>
            </tbody>
        </table>  
    </div>
    
</template>

<script>
import axios from 'axios'

export default {
    data(){
        return {
            students : []
        }
    },
    methods: {
        getStudentList() {
            axios({
                url: "http://localhost:8100/student",
                method: "GET"
            }).then(res =>{
                console.log(res.data)
                this.students = res.data
            })
        }

    }
   
}
</script>

